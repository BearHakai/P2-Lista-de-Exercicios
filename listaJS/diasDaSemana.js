//Pede para o usuario um valor para informar a data
var semana = parseInt(prompt("Informe um número e digo o dia da Semana correspondente: "));

//faz a decisao para infromar qual data sera
if(semana == 1){
    alert("Tenha um bom Domingo!");
    console.log("Tenha um bom Domingo!");
}

else if(semana == 2){
    alert("Tenha uma boa segunda-feira!");
    console.log("Tenha uma boa segunda-feira!");
}

else if(semana == 3){
    alert("Tenha uma boa terça-feira!");
    console.log("Tenha uma boa terça-feira!");
}

else if(semana == 4){
    alert("Tenha uma boa quarta-feira!");
    console.log("Tenha uma boa quarta-feira!");
}

else if(semana == 5){
    alert("Tenha uma boa quinta-feira");
    console.log("Tenha uma boa quinta-feira");
}

else if(semana == 6){
    alert("Tenha uma boa sexta-feira!");
    console.log("Tenha uma boa sexta-feira!");
}

else if(semana == 7){
    alert("Tena um bom Sábado!");
    console.log("Tena um bom Sábado!");
}

else{
    alert("Não é uma data válida!");
    console.log("Não é uma data válida!");
}